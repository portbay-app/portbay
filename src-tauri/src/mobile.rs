/// iOS launch: a pinned physical-device UDID takes the devicectl path; a
/// simulator UDID (or no pin) takes the simctl path. Both resolve
/// project/workspace and scheme at run time.
fn ios_command(cfg: &MobileRunConfig) -> String {
    // Pinned device udid/name (empty → auto). `-configuration` follows the
    // flavor when set, else Debug — and `auto_config` records that Debug was a
    // default, not a choice, so the device script may override it for Flutter
    // hosts (a pinned configuration is always honoured verbatim).
    let pinned_dev = cfg.device.clone().unwrap_or_default();
    let pinned_scheme = cfg.target.clone().unwrap_or_default();
    let (config, auto_config) = match cfg.flavor.as_deref() {
        Some(f) if !f.is_empty() => (f.to_string(), false),
        _ => ("Debug".to_string(), true),
    };

    if looks_like_ios_device_udid(&pinned_dev) {
        return ios_device_command(&pinned_dev, &pinned_scheme, &config, auto_config);
    }
    ios_simulator_command(&pinned_dev, &pinned_scheme, &config)
}

/// Shared shell fragment: resolve `$PF` (workspace/project flag) and `$SCHEME`.
///
/// Scheme fallback (no pin): prefer the scheme named like the workspace /
/// project itself (`Runner.xcworkspace` → `Runner`) — in a CocoaPods
/// workspace `xcodebuild -list` enumerates every pod's scheme alphabetically,
/// so "first listed" lands on an arbitrary pod (kitabi: built
/// `adaptive_platform_ui` instead of `Runner`, producing no .app). Only when
/// no same-named scheme exists fall back to the first listed one.
fn ios_project_resolve(pinned_scheme: &str) -> String {
    format!(
        "WS=$(ls -d *.xcworkspace 2>/dev/null | head -1); \
         PROJ=$(ls -d *.xcodeproj 2>/dev/null | head -1); \
         if [ -n \"$WS\" ]; then PF=\"-workspace $WS\"; else PF=\"-project $PROJ\"; fi; \
         SCHEME={pinned_scheme}; \
         if [ -z \"$SCHEME\" ]; then \
           if [ -n \"$WS\" ]; then BASE=$(basename \"$WS\" .xcworkspace); else BASE=$(basename \"$PROJ\" .xcodeproj); fi; \
           SCHEMES=$(xcodebuild $PF -list 2>/dev/null | awk '/Schemes:/{{f=1;next}} f{{if(!NF)exit; sub(/^ */,\"\"); print}}'); \
           SCHEME=$(printf '%s\\n' \"$SCHEMES\" | grep -Fx \"$BASE\" | head -1); \
           if [ -z \"$SCHEME\" ]; then SCHEME=$(printf '%s\\n' \"$SCHEMES\" | head -1); fi; \
         fi",
        pinned_scheme = sq(pinned_scheme),
    )
}

/// boot a simulator → build the scheme → install → launch attached to the
/// console. Resolves device at run time (pinned → first booted → first
/// available iPhone). The `.app` is picked from the `$CONFIG-iphonesimulator`
/// products dir specifically — a device build or another configuration's
/// stale product may sit next to it. `NSUnbufferedIO=YES` makes xcodebuild
/// write build output through the pipe per line instead of block-buffering,
/// so the log viewer narrates the (long) build phase live.
fn ios_simulator_command(pinned_dev: &str, pinned_scheme: &str, config: &str) -> String {
    format!(
        "set -e; \
         echo '::portbay::phase=resolving-device'; \
         {project_resolve}; \
         DEV={pinned_dev}; \
         if [ -n \"$DEV\" ] && ! xcrun simctl list devices available | grep -q \"$DEV\"; then \
           echo \"pinned simulator $DEV is unavailable — pick another destination in PortBay\" >&2; exit 1; fi; \
         if [ -z \"$DEV\" ]; then DEV=$(xcrun simctl list devices booted | grep -oE '[0-9A-Fa-f-]{{36}}' | head -1); fi; \
         if [ -z \"$DEV\" ]; then DEV=$(xcrun simctl list devices available | grep -i iPhone | grep -oE '[0-9A-Fa-f-]{{36}}' | head -1); fi; \
         if [ -z \"$DEV\" ]; then echo 'no iOS simulator found — create one in Xcode' >&2; exit 1; fi; \
         echo \"::portbay::device=$DEV\"; \
         echo '::portbay::phase=booting-device'; \
         xcrun simctl boot \"$DEV\" 2>/dev/null || true; \
         open -a Simulator || true; \
         CONFIG={config}; \
         echo '::portbay::phase=building'; \
         NSUnbufferedIO=YES xcodebuild $PF -scheme \"$SCHEME\" -configuration \"$CONFIG\" -destination \"id=$DEV\" -derivedDataPath .portbay-build build; \
         APP=$(find .portbay-build/Build/Products -maxdepth 2 -path \"*$CONFIG-iphonesimulator*\" -name '*.app' | head -1); \
         if [ -z \"$APP\" ]; then echo \"build succeeded but produced no .app — scheme '$SCHEME' is probably not the app scheme; pin the right one under Advanced → Scheme in PortBay\" >&2; exit 1; fi; \
         BID=$(/usr/libexec/PlistBuddy -c 'Print CFBundleIdentifier' \"$APP/Info.plist\"); \
         echo '::portbay::phase=installing'; \
         xcrun simctl install \"$DEV\" \"$APP\"; \
         printf 'PLATFORM=ios\\nDEV=%s\\nBID=%s\\n' \"$DEV\" \"$BID\" > {run_meta}; \
         echo '::portbay::phase=launching'; \
         exec xcrun simctl launch --console-pty \"$DEV\" \"$BID\"",
        project_resolve = ios_project_resolve(pinned_scheme),
        pinned_dev = sq(pinned_dev),
        config = sq(config),
        run_meta = RUN_META_FILE,
    )
}

/// Physical iOS device via `devicectl` (Xcode 15+): signing-aware build
/// (`-allowProvisioningUpdates` lets Xcode's automatic signing mint/refresh
/// the dev profile) → `devicectl device install app` → `devicectl device
/// process launch --console`, which stays attached for the run's lifetime
/// *and forwards catchable signals to the app* — so PC's SIGTERM on Stop
/// terminates the app on the device, matching Xcode's Stop. The reachability
/// pre-check keeps the never-substitute promise: an unplugged pinned phone is
/// an explicit error, not a fallback.
///
/// Flutter-host auto-Release (`auto_config` = no pinned configuration): a
/// *debug* Flutter app aborts at launch on a physical iOS 14+ device unless
/// Flutter tooling/Xcode is attached (the Dart JIT needs a debugger — the
/// kitabi signal-11 crash), and devicectl launches standalone. When the Xcode
/// project is a Flutter ios/ host (`../pubspec.yaml`), the script builds
/// Release instead — runnable standalone, like Xcode's ⌘R for release testing,
/// just without hot reload. Pinning a configuration in Advanced overrides.
/// The `.app` find is scoped to `$CONFIG-iphoneos` so a stale Debug product
/// next to the fresh Release one can't be picked.
fn ios_device_command(udid: &str, pinned_scheme: &str, config: &str, auto_config: bool) -> String {
    let flutter_release_switch = if auto_config {
        "if [ \"$CONFIG\" = 'Debug' ] && [ -f ../pubspec.yaml ]; then \
           echo 'Flutter host detected: debug builds cannot launch standalone on a device — building Release instead (pin a configuration in Advanced to override)'; \
           CONFIG='Release'; \
         fi; "
    } else {
        ""
    };
    format!(
        "set -e; \
         echo '::portbay::phase=resolving-device'; \
         {project_resolve}; \
         DEV={udid}; \
         if ! xcrun devicectl device info details --device \"$DEV\" >/dev/null 2>&1; then \
           echo \"device $DEV is not reachable — connect it by cable (unlock + Trust) or join the same Wi-Fi, then run again\" >&2; exit 1; fi; \
         echo \"::portbay::device=$DEV\"; \
         CONFIG={config}; \
         {flutter_release_switch}\
         echo '::portbay::phase=building'; \
         NSUnbufferedIO=YES xcodebuild $PF -scheme \"$SCHEME\" -configuration \"$CONFIG\" -destination \"id=$DEV\" -allowProvisioningUpdates -derivedDataPath .portbay-build build; \
         APP=$(find .portbay-build/Build/Products -maxdepth 2 -path \"*$CONFIG-iphoneos*\" -name '*.app' | head -1); \
         if [ -z \"$APP\" ]; then echo \"build succeeded but produced no .app — scheme '$SCHEME' is probably not the app scheme; pin the right one under Advanced → Scheme in PortBay\" >&2; exit 1; fi; \
         BID=$(/usr/libexec/PlistBuddy -c 'Print CFBundleIdentifier' \"$APP/Info.plist\"); \
         echo '::portbay::phase=installing'; \
         xcrun devicectl device install app --device \"$DEV\" \"$APP\"; \
         APPNAME=$(basename \"$APP\" .app); \
         printf 'PLATFORM=ios-device\\nDEV=%s\\nBID=%s\\nAPPNAME=%s\\n' \"$DEV\" \"$BID\" \"$APPNAME\" > {run_meta}; \
         echo '::portbay::phase=launching'; \
         exec xcrun devicectl device process launch --console --terminate-existing --device \"$DEV\" \"$BID\"",
        project_resolve = ios_project_resolve(pinned_scheme),
        udid = sq(udid),
        config = sq(config),
        flutter_release_switch = flutter_release_switch,
        run_meta = RUN_META_FILE,
    )
}

