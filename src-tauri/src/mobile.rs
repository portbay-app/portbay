/// `flutter run` stays attached for the session (hot-reload host). Honour a
/// pinned device and flavor when set; a pinned shut-down simulator/AVD is
/// booted first (flutter only runs on live targets). The pid file enables
/// hot reload/restart via SIGUSR1/SIGUSR2.
fn flutter_command(cfg: &MobileRunConfig) -> String {
    let pinned = cfg.device.clone().unwrap_or_default();
    let mut cmd = format!(
        "echo '::portbay::phase=resolving-device'; rm -f {pid}; ",
        pid = FLUTTER_PID_FILE
    );

    // Device argument for `flutter run`, after any boot prelude.
    let device_arg: Option<String> = if let Some(avd) = pinned.strip_prefix("avd:") {
        cmd.push_str(&android_avd_resolve(avd));
        cmd.push_str("; ");
        Some("-d \"$SER\"".to_string())
    } else if looks_like_sim_udid(&pinned) {
        cmd.push_str(&format!(
            "echo '::portbay::phase=booting-device'; \
             xcrun simctl boot {udid} 2>/dev/null || true; \
             open -a Simulator || true; ",
            udid = sq(&pinned),
        ));
        Some(format!("-d {}", sq(&pinned)))
    } else if !pinned.is_empty() {
        Some(format!("-d {}", sq(&pinned)))
    } else {
        None
    };

    cmd.push_str(&format!(
        "exec flutter run --pid-file {pid}",
        pid = FLUTTER_PID_FILE
    ));
    if let Some(d) = device_arg {
        cmd.push_str(&format!(" {d}"));
    }
    if let Some(f) = cfg.flavor.as_deref().filter(|s| !s.is_empty()) {
        cmd.push_str(&format!(" --flavor {}", sq(f)));
    }
    if let Some(t) = cfg.target.as_deref().filter(|s| !s.is_empty()) {
        cmd.push_str(&format!(" -t {}", sq(t)));
    }
    cmd
}

