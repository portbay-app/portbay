/// Shell fragment that resolves `$SER` for a running emulator backing `$AVD`,
/// boots the AVD when none is, and fails honestly when it can't.
fn android_avd_resolve(avd: &str) -> String {
    format!(
        "AVD={avd}; SER=''; \
         for S in $(adb devices | awk 'NR>1 && $2==\"device\"{{print $1}}' | grep '^emulator-' || true); do \
           N=$(adb -s \"$S\" emu avd name 2>/dev/null | head -1 | tr -d '\\r'); \
           if [ \"$N\" = \"$AVD\" ]; then SER=$S; break; fi; done; \
         if [ -z \"$SER\" ]; then echo '::portbay::phase=booting-device'; \
           (emulator -avd \"$AVD\" >/dev/null 2>&1 &); adb wait-for-device; \
           SER=$(adb devices | awk 'NR>1 && $2==\"device\"{{print $1; exit}}'); fi; \
         if [ -z \"$SER\" ]; then echo \"could not boot AVD $AVD — start it in Android Studio and retry\" >&2; exit 1; fi",
        avd = sq(avd),
    )
}

/// ensure a device/emulator → `gradlew install*` → launch the monkey
/// LAUNCHER intent → attach to `adb logcat`. Reads `applicationId` from the
/// app module's gradle for the launch intent. `--console=plain` stops Gradle
/// from drawing its rich in-place progress console (which buffers and
/// redraws ANSI blocks on a pipe) so build output reaches the log per line.
fn android_command(cfg: &MobileRunConfig) -> String {
    // Pinned serial or `avd:<name>` (empty → auto). A product `flavor`
    // prefixes the install task (`installStagingDebug`); the build *type* is
    // always Debug here, so a flavor of "debug"/"release" is treated as the
    // type — not doubled into `installDebugDebug`. No flavor → `installDebug`.
    let pinned = cfg.device.clone().unwrap_or_default();
    let product_flavor = cfg.flavor.as_deref().map(str::trim).filter(|f| {
        !f.is_empty() && !f.eq_ignore_ascii_case("debug") && !f.eq_ignore_ascii_case("release")
    });
    let install_task = match product_flavor {
        Some(f) => format!("install{}Debug", capitalize(f)),
        None => "installDebug".to_string(),
    };
    let module = match cfg.target.as_deref() {
        Some(t) if !t.is_empty() => t.to_string(),
        _ => "app".to_string(),
    };

    // Resolve $SER per pin form. A pinned serial that isn't connected is an
    // explicit error — never a silent fallback to some other device.
    let resolve = if let Some(avd) = pinned.strip_prefix("avd:") {
        android_avd_resolve(avd)
    } else if !pinned.is_empty() {
        format!(
            "SER={pinned}; \
             if ! adb devices | awk 'NR>1 && $2==\"device\"{{print $1}}' | grep -qx \"$SER\"; then \
               echo \"pinned device $SER is not connected — pick another destination in PortBay\" >&2; exit 1; fi",
            pinned = sq(&pinned),
        )
    } else {
        "SER=$(adb devices | awk 'NR>1 && $2==\"device\"{print $1; exit}'); \
         if [ -z \"$SER\" ]; then AVD=$(emulator -list-avds 2>/dev/null | grep -v '^INFO' | head -1); \
           if [ -n \"$AVD\" ]; then echo '::portbay::phase=booting-device'; \
             (emulator -avd \"$AVD\" >/dev/null 2>&1 &); adb wait-for-device; \
             SER=$(adb devices | awk 'NR>1 && $2==\"device\"{print $1; exit}'); \
           else echo 'no Android device/emulator — create an AVD in Android Studio' >&2; exit 1; fi; \
         fi"
            .to_string()
    };

    format!(
        "set -e; \
         echo '::portbay::phase=resolving-device'; \
         {resolve}; \
         echo \"::portbay::device=$SER\"; \
         GR=./gradlew; [ -x \"$GR\" ] || GR=gradlew; \
         echo '::portbay::phase=building'; \
         \"$GR\" --console=plain :{module}:{install_task}; \
         PKG=$(grep -RhoE 'applicationId[ =\"]+[a-zA-Z0-9_.]+' {module}/build.gradle* 2>/dev/null | grep -oE '[a-zA-Z0-9_.]+$' | head -1); \
         mkdir -p .portbay-build; \
         printf 'PLATFORM=android\\nSER=%s\\nPKG=%s\\n' \"$SER\" \"$PKG\" > {run_meta}; \
         echo '::portbay::phase=launching'; \
         if [ -n \"$PKG\" ]; then adb -s \"$SER\" shell monkey -p \"$PKG\" -c android.intent.category.LAUNCHER 1 || true; fi; \
         echo '::portbay::phase=connected'; \
         exec adb -s \"$SER\" logcat",
        resolve = resolve,
        module = module,
        install_task = install_task,
        run_meta = RUN_META_FILE,
    )
}

