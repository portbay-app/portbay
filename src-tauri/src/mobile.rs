//! Launch-command generation for mobile projects (iOS / Android / Flutter /
//! Expo).
//!
//! PortBay's Play/Stop is a Process Compose process: Play runs a long-running
//! `command`, Stop sends it SIGTERM. Native mobile "runs" aren't naturally
//! long-running (build → install → launch is one-shot), so each command here
//! ends by **attaching to a live stream** — `simctl launch --console-pty`,
//! `adb logcat`, `flutter run`, or the Expo Metro server. The stream keeps the
//! PC process alive while the app runs, and Stop tears it down.
//!
//! Device selection is resolved **at run time inside the shell** (not here), so
//! the command is self-contained and reflects whatever simulator/emulator the
//! user actually has booted: an explicit `MobileRunConfig.device` wins,
//! otherwise the first booted device, otherwise the first available one. This
//! keeps PC-config generation pure (no `simctl`/`adb` I/O in the reconcile
//! tick) and lets the same registry entry work across machines. A pinned
//! destination that's gone is an explicit, actionable error — the script never
//! silently substitutes another device (the Expo-Orbit bug we design against).
//!
//! Pinned-device id forms (written by the rail's destination picker, see
//! `crate::mobile_targets`): an iOS simulator udid, an adb serial, or
//! `avd:<name>` for an Android AVD the script boots first.
//!
//! Phase markers: the scripts echo line-anchored `::portbay::phase=<p>` /
//! `::portbay::device=<id>` markers between steps. `crate::mobile_phase`
//! parses them out of the log tail into the truthful run sub-state the UI
//! shows (booted → building → installing → launching → connected) instead of
//! flipping to "Running" while xcodebuild is still compiling.
//!
//! Stop metadata: before attaching, the iOS/Android scripts write
//! `.portbay-build/run-meta` (PLATFORM/DEV/BID or SER/PKG) so
//! [`terminate_app`] can kill the *app* on Stop — PC's SIGTERM only detaches
//! the log stream, which would leave the app running on the simulator
//! (Xcode's Stop kills the app; ours now does too).
//!
//! The shell here targets `/bin/sh` (what Process Compose invokes) and is kept
//! to one logical line via `&&`/`;`/`if…fi` so it round-trips through the YAML
//! `command` scalar cleanly. Paths are referenced relative to the project dir
//! (PC sets `working_dir` to the project path), sidestepping spaces in the
//! absolute path.

use std::collections::HashMap;
use std::path::Path;

use crate::registry::{MobileRunConfig, Project, ProjectType};

/// Relative path (inside the project dir) of the pid file `flutter run`
/// maintains for the session. Hot reload/restart signal this pid (SIGUSR1 /
/// SIGUSR2) — Flutter's documented headless reload mechanism, which works
/// through Process Compose where stdin doesn't.
pub const FLUTTER_PID_FILE: &str = ".portbay-flutter.pid";

/// Relative path of the stop metadata the iOS/Android scripts write.
pub const RUN_META_FILE: &str = ".portbay-build/run-meta";

/// Build the Play command for a mobile project, or `None` if the kind isn't a
/// mobile kind. The returned string is handed to Process Compose as the
/// process `command` and run from the project directory.
pub fn launch_command(p: &Project) -> Option<String> {
    let cfg = p.mobile_run.clone().unwrap_or_default();
    launch_command_for(p.kind, &cfg)
}

/// Core: the Play command for a mobile `kind` + its run config. Shared by the
/// reconciler (via [`launch_command`]) and the folder detector, so a mobile
/// project launches identically whether the command is generated at reconcile
/// time or stamped onto the registry at add time. Returns `None` for
/// non-mobile kinds.
pub fn launch_command_for(kind: ProjectType, cfg: &MobileRunConfig) -> Option<String> {
    match kind {
        ProjectType::Xcode => Some(ios_command(cfg)),
        ProjectType::Android => Some(android_command(cfg)),
        ProjectType::Flutter => Some(flutter_command(cfg)),
        ProjectType::Expo => Some(expo_command(cfg)),
        _ => None,
    }
}

/// True when `cmd` is a launch script generated by this module (any version):
/// only our scripts emit `::portbay::phase=` markers. The PC-config layer uses
/// this to regenerate a project's add-time `start_command` stamp instead of
/// running it verbatim forever — otherwise generator fixes never reach
/// already-added projects (kitabi: the registry kept replaying the old
/// first-listed-scheme script after the fallback was fixed). A user-written
/// custom command never contains the marker, so it still wins.
pub fn is_generated_launch(cmd: &str) -> bool {
    cmd.contains("::portbay::phase=")
}

/// True for kinds whose Play is a mobile simulator/emulator launch rather than
/// an HTTP dev server. Used by the PC-config layer to skip HTTP readiness
/// probes (a simulator launch never answers an HTTP GET).
pub fn is_mobile_kind(kind: ProjectType) -> bool {
    matches!(
        kind,
        ProjectType::Xcode | ProjectType::Android | ProjectType::Flutter | ProjectType::Expo
    )
}

/// Single-quote a value for safe interpolation into the `/bin/sh` command.
/// Empty stays empty (callers test for that). `'` is escaped the POSIX way.
fn sq(s: &str) -> String {
    format!("'{}'", s.replace('\'', "'\\''"))
}

/// Does this pinned device id look like a CoreSimulator UDID (uppercase UUID)?
/// Drives the Flutter prelude that boots a shut-down simulator before
/// `flutter run` (flutter refuses ids that aren't currently bootable).
fn looks_like_sim_udid(id: &str) -> bool {
    id.len() == 36
        && id.chars().all(|c| c.is_ascii_hexdigit() || c == '-')
        && id.chars().filter(|&c| c == '-').count() == 4
}

/// Does this pinned device id look like a *physical* iOS device UDID?
/// Modern devices: `00008120-001964AC0CB8C01E` (8 hex, dash, 16 hex);
/// pre-2018 devices: 40 hex chars, no dash. Distinct from the 36-char
/// 4-dash simulator UUIDs, so the iOS launch script can branch on the form.
fn looks_like_ios_device_udid(id: &str) -> bool {
    let modern = id.len() == 25
        && id.as_bytes().get(8) == Some(&b'-')
        && id.chars().enumerate().all(|(i, c)| {
            if i == 8 {
                c == '-'
            } else {
                c.is_ascii_hexdigit()
            }
        });
    let legacy = id.len() == 40 && id.chars().all(|c| c.is_ascii_hexdigit());
    modern || legacy
}

