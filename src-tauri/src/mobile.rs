/// Kill the *app* a mobile run launched, using the `run-meta` the script wrote
/// — Xcode's Stop semantics. PC's SIGTERM only tears down the attached stream
/// (console-pty / logcat); without this the app keeps running on the
/// simulator/device. Best-effort and blocking (callers use the blocking pool);
/// the meta file is consumed so a stale file can't kill a later run's app.
pub fn terminate_app(project_dir: &Path) {
    let meta_path = project_dir.join(RUN_META_FILE);
    let Ok(text) = std::fs::read_to_string(&meta_path) else {
        return;
    };
    let kv = parse_run_meta(&text);
    match kv.get("PLATFORM").map(String::as_str) {
        Some("ios") => {
            if let (Some(dev), Some(bid)) = (kv.get("DEV"), kv.get("BID")) {
                if !dev.is_empty() && !bid.is_empty() {
                    let _ = std::process::Command::new("xcrun")
                        .args(["simctl", "terminate", dev, bid])
                        .output();
                }
            }
        }
        // Physical device: the primary stop path is `devicectl … launch
        // --console` forwarding PC's SIGTERM to the app. This fallback covers
        // the escalation case (PC SIGKILLs a hung devicectl after its grace
        // window — nothing gets forwarded): look the app's pid up on the
        // device and terminate it there.
        Some("ios-device") => {
            if let (Some(dev), Some(appname)) = (kv.get("DEV"), kv.get("APPNAME")) {
                if !dev.is_empty() && !appname.is_empty() {
                    if let Some(pid) = device_app_pid(dev, appname) {
                        let _ = std::process::Command::new("xcrun")
                            .args([
                                "devicectl",
                                "device",
                                "process",
                                "terminate",
                                "--device",
                                dev,
                                "--pid",
                                &pid.to_string(),
                            ])
                            .output();
                    }
                }
            }
        }
        Some("android") => {
            if let (Some(ser), Some(pkg)) = (kv.get("SER"), kv.get("PKG")) {
                if !ser.is_empty() && !pkg.is_empty() {
                    if let Some(adb) = crate::mobile_targets::adb_bin() {
                        let _ = std::process::Command::new(adb)
                            .args(["-s", ser, "shell", "am", "force-stop", pkg])
                            .output();
                    }
                }
            }
        }
        _ => {}
    }
    let _ = std::fs::remove_file(&meta_path);
}

/// Pid of `<appname>.app/<appname>` running on a physical device, via
/// `devicectl device info processes` (JSON goes to a temp file — that's
/// devicectl's only scripting interface). Best-effort: any failure → None.
fn device_app_pid(dev: &str, appname: &str) -> Option<u32> {
    let tmp = std::env::temp_dir().join(format!("portbay-devprocs-{}.json", std::process::id()));
    let ok = std::process::Command::new("xcrun")
        .args([
            "devicectl",
            "device",
            "info",
            "processes",
            "--device",
            dev,
            "--json-output",
        ])
        .arg(&tmp)
        .output()
        .map(|o| o.status.success())
        .unwrap_or(false);
    if !ok {
        let _ = std::fs::remove_file(&tmp);
        return None;
    }
    let json = std::fs::read_to_string(&tmp).unwrap_or_default();
    let _ = std::fs::remove_file(&tmp);
    parse_device_app_pid(&json, appname)
}

pub(crate) fn parse_device_app_pid(json: &str, appname: &str) -> Option<u32> {
    let v: serde_json::Value = serde_json::from_str(json).ok()?;
    let procs = v.pointer("/result/runningProcesses")?.as_array()?;
    let needle = format!("/{appname}.app/{appname}");
    procs.iter().find_map(|p| {
        let exe = p.get("executable")?.as_str()?;
        if exe.ends_with(&needle) {
            p.get("processIdentifier")?.as_u64().map(|pid| pid as u32)
        } else {
            None
        }
    })
}

/// Parse the `KEY=VALUE` lines of a run-meta file.
pub(crate) fn parse_run_meta(text: &str) -> HashMap<String, String> {
    text.lines()
        .filter_map(|l| {
            let (k, v) = l.split_once('=')?;
            Some((k.trim().to_string(), v.trim().to_string()))
        })
        .collect()
}

/// Pid of the live `flutter run` session for a project, from the pid file the
/// launch command maintains. `None` when no session is running (or the file is
/// stale-empty).
pub fn flutter_run_pid(project_dir: &Path) -> Option<i32> {
    let text = std::fs::read_to_string(project_dir.join(FLUTTER_PID_FILE)).ok()?;
    let pid: i32 = text.trim().parse().ok()?;
    (pid > 1).then_some(pid)
}

